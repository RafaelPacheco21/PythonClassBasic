#Convertir a mayusculas un texto ingresado por el usuario 

dato=input('Ingresa el texto por favor: ')

dato_mayusculas=dato.upper()
print('La informacion convertida a mayúsculas es: ',dato_mayusculas)
#para convertir en minusculas
dato_minusculas=dato_mayusculas.lower()
print('La informacion convertida a minúsculas es: ',dato_minusculas)